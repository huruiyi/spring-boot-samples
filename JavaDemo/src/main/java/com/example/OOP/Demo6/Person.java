package com.example.OOP.Demo6;

public class Person {
	String name;
	int age;
	String sex;

	public String showName() {
		return name;
	}

	public void setName(String theName) {
		name = theName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int theAge) {
		age = theAge;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String theSex) {
		sex = theSex;
	}
}
